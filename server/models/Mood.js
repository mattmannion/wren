const { Schema, model } = require('mongoose');

const moodSchema = new Schema(
  {
    moodText: {
      type: String,
      required: 'You need to leave a thought!',
      minlength: 1,
      maxlength: 280
    },
    createdAt: {
      type: Date,
      default: Date.now,
      get: timestamp => dateFormat(timestamp)
    },
    username: {
      type: String,
      required: true
    },
  },
  {
    toJSON: {
      getters: true
    }
  }
);

const Mood = model('Mood', moodSchema);

module.exports = Mood;